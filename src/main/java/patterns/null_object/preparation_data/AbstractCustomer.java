package patterns.null_object.preparation_data;

/**
 * The type Abstract customer.
 */
public abstract class AbstractCustomer {
    /**
     * The Name.
     */
    protected String name;

    /**
     * Is nil boolean.
     *
     * @return the boolean
     */
    public abstract boolean isNil();

    /**
     * Gets name.
     *
     * @return the name
     */
    public abstract String getName();
}
