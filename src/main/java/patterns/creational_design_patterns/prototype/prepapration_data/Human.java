package patterns.creational_design_patterns.prototype.prepapration_data;

public class Human implements CopyAble {
    int age;
    String name;

    public Human(int age, String name) {
        this.age = age;
        this.name = name;
    }

    @Override
    public Object copy() {
        Human duplicate = new Human(age, name);
        return duplicate;
    }

    @Override
    public String toString() {
        return "Human{" +
                "age=" + age +
                ", name='" + name + '\'' +
                '}';
    }
}
